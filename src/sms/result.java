    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       
   
        String sid=id.getText();//id
        String nam=name.getText();//username
        String cls=cl.getText();//class
        
        String eng = e.getText();
        double en= Double.parseDouble(eng);
        String mat = m.getText();
        double ma= Double.parseDouble(mat);
        String sci = s.getText();
        double sc= Double.parseDouble(sci);
        String rel= r.getText();
        double re= Double.parseDouble(rel);
        String ban= b.getText();
        double ba= Double.parseDouble(ban);
        
        double total=en+ba+re+sc+ma;
        double av=total/5;
        
        String grade;
        
        if(av>= 80){
        grade= "A+";
        }
        else if(av>=70 && av <80){
        grade="A";
        }
        
        else if(av>=60 && av <70){
        grade="B";
        }
        
        else if(av>=50 && av<60){
        grade="C";
        }
        
        else if(av>=40 && av<50){
        grade="D";
        }
        else
        { 
            grade="Fail";
        }
        
        t.setText("\t\n"+grade);

    }//GEN-LAST:event_jButton1MouseClicked
  /**
   * stores the marks into database
   */
    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String sid=id.getText();//id
        String nam=name.getText();//username
        String cls=cl.getText();//class
        
        String eng = e.getText();
        double en= Double.parseDouble(eng);
        String mat = m.getText();
        double ma= Double.parseDouble(mat);
        String sci = s.getText();
        double sc= Double.parseDouble(sci);
        String rel= r.getText();
        double re= Double.parseDouble(rel);
        String ban= b.getText();
        double ba= Double.parseDouble(ban);
        
        double total=en+ba+re+sc+ma;
        double av=total/5;
        
        String grade;
        
        if(av>= 80){
        grade= "A+";
        }
        else if(av>=70 && av <80){
        grade="A";
        }
        
        else if(av>=60 && av <70){
        grade="B";
        }
        
        else if(av>=50 && av<60){
        grade="C";
        }
        
        else if(av>=40 && av<50){
        grade="D";
        }
        else
        { grade="Fail";
        }
        

        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/scl","root","");
        String sql= "insert into result values(?,?,?,?,?,?,?,?,?)";
        PreparedStatement ptst = conn.prepareStatement(sql);
        
        ptst.setString(1,id.getText());
        ptst.setString(2,name.getText());
        ptst.setString(3,cl.getText());
        ptst.setDouble(4,en);
        ptst.setDouble(5,ma);
        ptst.setDouble(6,sc);
        ptst.setDouble(7,ba);
        ptst.setDouble(8,re);
        ptst.setString(9,grade);
        ptst.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Data has been stored successfully!");
        conn.close();
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2MouseClicked
